// Read text in a local route

function readText(local_route) {
    var text = null;
    var xmlhttp = new XMLHttpRequest();
    xmlhttp.open("GET", local_route, false);
    xmlhttp.send();

    if (xmlhttp.status == 200) {
        text = xmlhttp.responseText;
    }

    return text;
}